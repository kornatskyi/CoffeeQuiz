let weights = [];
let customerResult = [];
let resultString = "";
/** Count most weight result.*/
function showMostWeightResult() {
    countWeights();

    for (let i = 0; i < results.length; i++) {
        weights.push(results[i].weight);
    }
    results.forEach((element) => {
        if (element.weight === Math.max.apply(Math, weights)) {
            customerResult.push(element);
            resultString += `<h2>${element.name}</h2><br/>`;
        }
    });
    for (let j = 0; j < customerResult.length; j++) {
        $('#result-container').html($('#result-container').html() + customerResult[j].product);

    }
    document.getElementById('result-container').hidden = false;

}

